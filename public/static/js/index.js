    xml2json: function (xml) {

      const parseBounds = (text) => {
        const m = text.match(/\[(\d+),(\d+)\]\[(\d+),(\d+)\]/);
        if (!m) return null;
        const [_, lx, ly, rx, ry] = m.map(Number);
        return { x: lx, y: ly, width: rx - lx, height: ry - ly };
      };

      const safeXmlStr = (s) => s.replace(/\$/g, "-");

      const strToBool = (v) => ["yes", "true", "t", "1"].includes(v.toLowerCase());

      const strToInt = (v) => parseInt(v, 10);

      const convStr = (v) => v;

      const alias = {
        'class': '_type',
        'resource-id': 'resourceId',
        'content-desc': 'description',
        'long-clickable': 'longClickable',
        'bounds': 'rect',
      };

      const parsers = {
        '_type': safeXmlStr,
        'rect': parseBounds,
        'text': convStr,
        'resourceId': convStr,
        'package': convStr,
        'checkable': strToBool,
        'checked': strToBool,
        'scrollable': strToBool,
        'focused': strToBool,
        'clickable': strToBool,
        'selected': strToBool,
        'longClickable': strToBool,
        'focusable': strToBool,
        'password': strToBool,
        'index': strToInt,
        'description': convStr,
        'name': convStr,
        'label': convStr,
        'x': strToInt,
        'y': strToInt,
        'width': strToInt,
        'height': strToInt,
        'enabled': strToBool,
      },

        parseUiAutomatorNode = (node) => {
          const ks = {};
          const nameNodeMap = node.attributes;
          for (const attrs of Object.entries(nameNodeMap)) {
            const k = alias[attrs[1].nodeName] || attrs[1].nodeName;
            const f = parsers[k];
            if (attrs[1] === null) {
              ks[k] = null;
            } else if (f) {
              ks[k] = f(attrs[1].nodeValue);
            }
          }

          if (ks['bounds']) {
            const [lx, ly, rx, ry] = ks['bounds'].map(Number);
            ks['rect'] = { x: lx, y: ly, width: rx - lx, height: ry - ly };
            delete ks['bounds'];
          }
          return ks;
        },

        generateUuid = () => {
          // 简单实现生成 UUID 的逻辑
          return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function (c) {
            var r = Math.random() * 16 | 0,
              v = c === 'x' ? r : (r & 0x3 | 0x8);
            return v.toString(16);
          });
        },

        androidHierarchyToJson = (pageXml) => {
          // console.log(pageXml)
          // const dom = new DOMParser().parseFromString(pageXml, 'text/xml');
          const root = pageXml.documentElement;

          const travel = (node) => {
            if (!node.attributes) {
              return;
            }
            const jsonNode = parseUiAutomatorNode(node);
            // console.log(node, "->", JSON.stringify(jsonNode))

            jsonNode['_id'] = generateUuid();
            if (jsonNode['_type'] !== 'android.webkit.WebView' && node.childNodes) {
              const children = [];
              for (const n of node.childNodes) {
                const child = travel(n);
                if (child) children.push(child);
              }
              jsonNode['children'] = children;
            }
            // console.log(node, "->", JSON.stringify(jsonNode))
            return jsonNode;
          };

          return travel(root);
        }
      return androidHierarchyToJson(xml);
    },
